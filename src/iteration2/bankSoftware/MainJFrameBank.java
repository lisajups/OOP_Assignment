    private double power(int input1, int input2){
        double output;
        output = input1;
        int i;
        for (i=0; i<input2-1; i++){
            output = output*input1;
        }
        return output;
    }
    private double round(double input){
        double remainder = input%0.005;
        if (input%1 != 0){
            input = input - remainder;
        }
        return input;
    }
