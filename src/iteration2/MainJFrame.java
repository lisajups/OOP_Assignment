    private void CalculateOrderTotalButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalculateOrderTotalButtonActionPerformed
        //Displays the order total (£)
        orderQuantity = Integer.valueOf(OrderQuantityTextField.getText());
        if (orderQuantity>=1){
            if((searchProduct.getMaxStock()>=(orderQuantity+searchProduct.getStock()))){
                //Cannot order more than the previously defined maximum stock
                subtotalLabel.setText("Subtotal: £" + (orderQuantity)*searchProduct.getPrice());
                orderTotalLabel.setText("Total: £" +((orderQuantity)*searchProduct.getPrice()+1.5));
                DeliveryCostLabel.setText("Delivery costs: £1.50");
                OrderButton.setEnabled(true);
            }
            else{
                System.out.println("Too many items ordered");
                OrderButton.setEnabled(false);
            }
        }
        else{
            System.out.println("Order can't be less than 1");
            OrderButton.setEnabled(false);
        }
        
    }//GEN-LAST:event_CalculateOrderTotalButtonActionPerformed

