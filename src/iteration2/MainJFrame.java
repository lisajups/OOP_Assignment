    private void productSearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_productSearchButtonActionPerformed
        
        searchProduct = new Product();
        //Create object to hold search attributes
        if (!"".equals(jSearchNameTextField.getText())){
            searchProduct.setName(jSearchNameTextField.getText());
            //If name field has text, set product name to textfield value
        }
        if (!"".equals(jSearchIDTextField.getText())){
            searchProduct.setID(Integer.valueOf(jSearchIDTextField.getText()));
            //If ID field has text, set product ID to textfield value
        }
        searchProduct = productList.find(searchProduct, displayTextArea);
        if (productList.isProduct(searchProduct)){
            //If product exists enable buttons to buy (if customer) and edit (if owner) product
            SearchProductDisplayImageLabel.setIcon(new ImageIcon(searchProduct.getImage()));
            OpenOrderButton.setEnabled(true);
            OpenEditProductButton.setEnabled(true);
            RemoveProductButton.setEnabled(true);
            jSearchAddToBasketButton.setEnabled(true);
            jSearchAddToWishlistButton.setEnabled(true);
        }
        else{
            OpenOrderButton.setEnabled(false);
            OpenEditProductButton.setEnabled(false);
            RemoveProductButton.setEnabled(false);
            jSearchAddToBasketButton.setEnabled(false);
        }
        

    }//GEN-LAST:event_productSearchButtonActionPerformed

