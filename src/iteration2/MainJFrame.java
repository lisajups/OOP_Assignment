    private void jPurchaseBasketButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jPurchaseBasketButtonActionPerformed
        //Finds the customer account, makes the transaction for the order amount, and then updates the stock of purchased products
        if (customerList.findCustAccount(jCheckoutSortCode.getText(), Integer.valueOf(jCheckoutAccountNum.getText()))!= null){
            custAccount = customerList.findCustAccount(jCheckoutSortCode.getText(), Integer.valueOf(jCheckoutAccountNum.getText()));
            //If customer account exists, load it as custAccount object
            System.out.println("Account found");
            if (custAccount.getType().equals("Current Account")){
                Account shopAccount = customerList.findCustAccount("01-01-21",987654321);
                System.out.println("Customer balance: " + custAccount.getBalance());
                System.out.println("Shop balance: " + shopAccount.getBalance());
                custAccount.transfer(shopAccount, total);
                System.out.println("Customer balance: " + custAccount.getBalance());
                System.out.println("Shop balance: " + shopAccount.getBalance());
                for (int i = 0; i<basket.size(); i++){
                    //For item purchased, update stock
                    String item = basket.get(i);
                    String lines[] = item.split("\\n");
                    Product basketProduct = new Product();
                    basketProduct.setID(Integer.valueOf(lines[0]));
                    basketProduct.setName(lines[1]);
                    basketProduct = productList.find(basketProduct);
                    if (productList.isProduct(basketProduct)){
                        productList.find(basketProduct).purchase(Integer.valueOf(lines[2]));
                        productList.Remove(basketProduct);
                        productList.SaveToFile();
                        basketProduct.SaveToFile(true);
                        productList.Add(basketProduct);
                    }    
                }
            }
            else{
                System.out.println("Not a current account");
            }
            
        }
        else{
            System.out.println("Account not found");
        }
    }//GEN-LAST:event_jPurchaseBasketButtonActionPerformed

    private void UpdateWishlistButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateWishlistButtonActionPerformed
        //Displays up-to-date wishlist
        WishlistList.clearSelection();
        wishlistInfo.clear();
        WishlistList.removeAll();
        for (int i = 0; i<wishlist.size(); i++){
            String item = wishlist.get(i);
            String lines[] = item.split("\\n");
            Product wlProduct = new Product();
            wlProduct.setID(Integer.valueOf(lines[0]));
            wlProduct.setName(lines[1]);
            wlProduct = productList.find(wlProduct);
            if (productList.isProduct(wlProduct)){
                wishlistInfo.add(wlProduct.wishlistString());
            }     
        }
        WishlistList.setListData(wishlistInfo.toArray(new String[wishlistInfo.size()]));
        WishlistList.clearSelection();
    }//GEN-LAST:event_UpdateWishlistButtonActionPerformed

    private void UpdateBasketButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateBasketButtonActionPerformed
        //Displays up-to-date basket
        basketInfo.clear();
        total = 0;
        for (int i = 0; i<basket.size(); i++){
            String item = basket.get(i);
            String lines[] = item.split("\\n");
            Product basketProduct = new Product();
            basketProduct.setID(Integer.valueOf(lines[0]));
            basketProduct.setName(lines[1]);
            basketProduct = productList.find(basketProduct);
            if (productList.isProduct(basketProduct)){
                basketInfo.add(basketProduct.basketString(Integer.valueOf(lines[2])));
                total = total + basketProduct.getPrice()*Integer.valueOf(lines[2]);
            }    
        }
        jTotalLabel.setText("Total: £" + total);
        BasketList.setListData(basketInfo.toArray(new String[basketInfo.size()]));     
        jPurchaseBasketButton.setEnabled(true);
    }//GEN-LAST:event_UpdateBasketButtonActionPerformed

